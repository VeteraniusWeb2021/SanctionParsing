class Address():
    def __init__(self, general_id, city, country, full, latitude, longitude, post_office_box, postal_code, region,
                 remarks, state, street, street2, things):
        self.general_id = general_id
        self.city = city
        self.country = country
        self.full = full
        self.latitude = latitude
        self.longitude = longitude
        self.post_office_box = post_office_box
        self.postal_code = postal_code
        self.region = region
        self.remarks = remarks
        self.state = state
        self.street = street
        self.street2 = street2
        self.things = things