class Vehicle():
    def __init__(self, general_id, build_date, model, operator, owner, registration_date, registration_number, type):
        self.general_id = general_id
        self.build_date = build_date
        self.model = model
        self.operator = operator
        self.owner = owner
        self.registration_date = registration_date
        self.registration_number = registration_number
        self.type = type